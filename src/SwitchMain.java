
public class SwitchMain {

	public static void main(String[] args) {
		
		int x = 0;
		
		
		switch(x) {
		
		case 1:
		case 2:
			System.out.println("1-2");
			break;
		case 3:
		case 4:
			System.out.println("3-4");
			break;
		default:
			System.out.println("Not 1-4");
			break;
		
		
		}
		
		
		
	}
	
	
}
